/// ToastPosition usado para definir a posição da torrada na tela
/// Veja AnterrostOast
enum AnterosToastPosition {
  /// [AnterosToastPosition.TOP] é usado para mostrar torradas no topo da tela
  TOP,

  /// [AnterosToastPosition.BOTTOM] é usado para mostrar torradas na parte inferior da tela
  BOTTOM,

  /// [AnterosToastPosition.CENTER] é usado para mostrar o centro de torradas da tela
  CENTER,

  /// [AnterosToastPosition.TOP_LEFT] é usado para mostrar torrada no canto superior esquerdo da tela
  TOP_LEFT,

  /// [AnterosToastPosition.TOP_RIGHT] é usado para mostrar torradas no canto superior direito da tela
  TOP_RIGHT,

  /// [AnterosToastPosition.BOTTOM_LEFT] é usado para mostrar torradas no canto inferior esquerdo da tela
  BOTTOM_LEFT,

  /// [AnterosToastPosition.BOTTOM_RIGHT] é usado para mostrar torradas no canto inferior direito da tela
  BOTTOM_RIGHT,

  /// [AnterosToastPosition.CENTER_LEFT] é usado para mostrar o Toast Center à esquerda da tela
  CENTER_LEFT,

  /// [AnterosToastPosition.CENTER_RIGHT] é usado para mostrar o Toast Center à direita da tela
  CENTER_RIGHT,
}
