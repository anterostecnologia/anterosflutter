/// ToastPosition Used to define the position of the Toast on the screen
/// See AnterosToast
enum AnterosToastPosition {
  /// [AnterosToastPosition.TOP] is used to show toast top of the screen
  TOP,

  /// [AnterosToastPosition.BOTTOM] is used to show toast bottom of the screen
  BOTTOM,

  /// [AnterosToastPosition.CENTER] is used to show toast center of the screen
  CENTER,

  /// [AnterosToastPosition.TOP_LEFT] is used to show toast top left of the screen
  TOP_LEFT,

  /// [AnterosToastPosition.TOP_RIGHT] is used to show toast top right of the screen
  TOP_RIGHT,

  /// [AnterosToastPosition.BOTTOM_LEFT] is used to show toast bottom left of the screen
  BOTTOM_LEFT,

  /// [AnterosToastPosition.BOTTOM_RIGHT] is used to show toast bottom right of the screen
  BOTTOM_RIGHT,

  /// [AnterosToastPosition.CENTER_LEFT] is used to show toast center left of the screen
  CENTER_LEFT,

  /// [AnterosToastPosition.CENTER_RIGHT] is used to show toast center right of the screen
  CENTER_RIGHT,
}
